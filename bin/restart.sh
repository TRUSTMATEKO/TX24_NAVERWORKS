#!/bin/bash
#==============================================================================
# Application Restart Script
#==============================================================================

#==============================================================================
# 설정 변수
#==============================================================================
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

#==============================================================================
# Main
#==============================================================================
main() {
    cd "$SCRIPT_DIR" || exit 1
    
    echo "Restarting application..."
    echo ""
    
    # Stop
    if [ -f "./stop.sh" ]; then
        ./stop.sh
    else
        echo "Error: stop.sh not found"
        exit 1
    fi
    
    echo ""
    sleep 2
    
    # Start
    if [ -f "./start.sh" ]; then
        ./start.sh "$@"
    else
        echo "Error: start.sh not found"
        exit 1
    fi
}

main "$@"